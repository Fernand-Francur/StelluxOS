void clear_screen() {
    uint32_t i;
    for (i = 0; i < size; i++) {
        uint32_t offset = i * bytes_per_pixel;
        pixels[offset + 0] = 0x1F; // Red
        pixels[offset + 1] = 0x1F; // Green
        pixels[offset + 2] = 0x1F; // Blue
        if (bytes_per_pixel == 4) {
            pixels[offset + 3] = 0xFF; // Alpha (if present)
        }
    }
}

void init_framebuffer_renderer() {
    uint32_t framebuffer_page_count;

    bytes_per_pixel = g_mbi_framebuffer->common.framebuffer_bpp / 8;
    framebuffer_page_count =
        (g_mbi_framebuffer->common.framebuffer_width * g_mbi_framebuffer->common.framebuffer_height * bytes_per_pixel) /
        PAGE_SIZE;

    size = framebuffer_page_count * PAGE_SIZE / bytes_per_pixel;
    uintptr_t gop_addr = g_mbi_framebuffer->common.framebuffer_addr;

    pixels = (char*)vmm::map_contiguous_physical_pages(gop_addr, framebuffer_page_count, PTE_DEFAULT_KERNEL_FLAGS | PTE_PAT);

    serial::printf("------------ Framebuffer ------------\n");
    serial::printf("      physbase            : 0x%llx\n", g_mbi_framebuffer->common.framebuffer_addr);
    serial::printf("      virtbase            : 0x%llx\n", pixels);
    serial::printf("      framebuffer->pitch  : %u\n", g_mbi_framebuffer->common.framebuffer_pitch);
    serial::printf("      framebuffer->width  : %u\n", g_mbi_framebuffer->common.framebuffer_width);
    serial::printf("      framebuffer->height : %u\n", g_mbi_framebuffer->common.framebuffer_height);
    serial::printf("      framebuffer->bpp    : %u\n\n", g_mbi_framebuffer->common.framebuffer_bpp);

    clear_screen();
}

void fill_pixel(uint32_t x, uint32_t y, uint32_t color) {
    uint32_t pitch = g_mbi_framebuffer->common.framebuffer_pitch; // Distance between rows in bytes
    uint32_t offset = (y * pitch) + (x * bytes_per_pixel);

    // Write color based on bytes_per_pixel
    pixels[offset + 0] = (color >> 0) & 0xFF;  // Blue
    pixels[offset + 1] = (color >> 8) & 0xFF;  // Green
    pixels[offset + 2] = (color >> 16) & 0xFF; // Red
    if (bytes_per_pixel == 4) {
        pixels[offset + 3] = (color >> 24) & 0xFF; // Alpha (if present)
    }
}

void render_text_glyph(char c, uint32_t x, uint32_t y, uint32_t color) {
    int row;
    int col;
    uint8_t glyph_row;

    // Get the font glyph data offset for the character
    const uint8_t *glyph = &psf_font_data[4 + (c * FONT_HEIGHT)];

    // Render the glyph onto the framebuffer
    for (row = 0; row < FONT_HEIGHT; row++) {
        glyph_row = glyph[row];
        for (col = 0; col < FONT_WIDTH; col++) {
            if (glyph_row & (1 << (7 - col))) {
                int pixel_x = x + col;
                int pixel_y = y + row;

                fill_pixel(pixel_x, pixel_y, color);
            }
        }
    }
}

void render_string(const char *str) {
    while (*str) {
        if (*str == '\n') {
            // Move to the next line
            cursor_y += FONT_HEIGHT;
            cursor_x = 0;
        } else {
            // Render the character
            render_text_glyph(*str, cursor_x, cursor_y, TEXT_COLOR);
            cursor_x += FONT_WIDTH;

            // Check for line wrapping
            if (cursor_x + FONT_WIDTH > g_mbi_framebuffer->common.framebuffer_width) {
                cursor_x = 0;
                cursor_y += FONT_HEIGHT;
            }
        }
        str++;
    }
}

template <typename... Args>
int gop_printf(const char* format, Args... args) {
    // Define a buffer with a fixed size. Adjust as necessary.
    constexpr size_t BUFFER_SIZE = 256;
    char buffer[BUFFER_SIZE] = { 0 };
    
    // Format the kstl::string using the custom sprintf
    int len = sprintf(buffer, BUFFER_SIZE, format, args...);
    
    // Send the formatted kstl::string over COM1
    render_string(buffer);
    
    return len;
}

void walk_mbi(void* mbi) {
    // Cast the mbi pointer to a byte pointer for arithmetic
    uint8_t* ptr = static_cast<uint8_t*>(mbi);
    uint32_t total_size = *reinterpret_cast<uint32_t*>(ptr);

    // Move pointer past the initial 8 bytes
    ptr += 8;

    // Calculate the end of the MBI structure
    uint8_t* mbi_end = static_cast<uint8_t*>(mbi) + total_size;

    while (ptr < mbi_end) {
        // Interpret the current position as a multiboot_tag
        multiboot_tag* tag = reinterpret_cast<multiboot_tag*>(ptr);

        // Process the tag based on its type
        switch (tag->type) {
            case MULTIBOOT_TAG_TYPE_CMDLINE: {
                multiboot_tag_string* cmdline_tag = reinterpret_cast<multiboot_tag_string*>(tag);
                g_mbi_kernel_cmdline = cmdline_tag->string;
                break;
            }
            case MULTIBOOT_TAG_TYPE_FRAMEBUFFER: {
                g_mbi_framebuffer = reinterpret_cast<multiboot_tag_framebuffer*>(tag);
                break;
            }
            case MULTIBOOT_TAG_TYPE_EFI_MMAP: { // New case for EFI Memory Map
                g_mbi_efi_mmap = reinterpret_cast<void*>(tag);
                break;
            }
            case MULTIBOOT_TAG_TYPE_ACPI_NEW: {
                g_mbi_acpi_rsdp = reinterpret_cast<multiboot_tag_new_acpi*>(tag)->rsdp;
                break;
            }
            default: {
                break;
            }
        }

        // Move to the next tag, ensuring 8-byte alignment
        ptr += (tag->size + 7) & ~7;
    }
}

